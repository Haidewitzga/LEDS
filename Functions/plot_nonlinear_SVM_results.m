function plot_nonlinear_SVM_results( svm_model, U, Y, C_hat, classification_error)

    arguments
        svm_model
        U (:,2) double
        Y (:,1)
        C_hat (:,1)
        classification_error (1,1) double
    end

    classes = unique(Y);
    M = numel(classes);

    %% Create grid for final multiclass boundary
    padding_1 = 0.05 * range(U(:,1));
    padding_2 = 0.05 * range(U(:,2));

    u_1 = linspace( ...
        min(U(:,1)) - padding_1, ...
        max(U(:,1)) + padding_1, ...
        300);

    u_2 = linspace( ...
        min(U(:,2)) - padding_2, ...
        max(U(:,2)) + padding_2, ...
        300);

    [U_1, U_2] = meshgrid(u_1, u_2);
    U_grid = [U_1(:), U_2(:)];

    % Final multiclass prediction on the grid
    [C_grid, ~] = apply_nonlinear_SVM_classification(svm_model, U_grid);

    [~, grid_class_index] = ismember(C_grid, classes);
    class_map = reshape(grid_class_index, size(U_1));

    %% Plot
    figure;
    hold on;

    % Plot points according to their TRUE class
    for i = 1:M

        true_index = Y == classes(i);

        scatter( ...
            U(true_index,1), ...
            U(true_index,2), ...
            12, ...
            'filled', ...
            'DisplayName', ...
            ['$C_' num2str(i) '$']);

    end

    % Highlight misclassified points
    misclassified = C_hat ~= Y;

    scatter( ...
        U(misclassified,1), ...
        U(misclassified,2), ...
        50, ...
        'o', ...
        'MarkerEdgeColor', 'k', ...
        'LineWidth', 1.2, ...
        'DisplayName', 'Misclassified');

    % Plot final multiclass decision boundaries
    contour( ...
        U_1, ...
        U_2, ...
        class_map, ...
        1.5:1:(M - 0.5), ...
        ':r', ...
        'LineWidth', 1.2, ...
        'HandleVisibility', 'off');

    xlabel('$u_1$', 'Interpreter', 'latex');
    ylabel('$u_2$', 'Interpreter', 'latex');

    title( ...
        sprintf( ...
            'Test set: classification error = %.2f\\%%', ...
            100 * classification_error), ...
        'Interpreter', 'latex');

    legend( ...
        'Interpreter', 'latex', ...
        'Location', 'best');

    grid on;
    box on;
    axis equal;
    hold off;

end